const day = 2;
let text;

switch (day) {
    case 0:
        text = "Domingo";
        break;
    case 1:
        text = "Lunes";
        break;
    case 2:
        text = "Martes";
        break;
    case 3:
        text = "Miercoles";
        break;
    case 4:
        text = "Jueves";
        break;
    case 5:
        text = "Viernes";
        break;
    case 6:
        text = "Sabado";
        break;
    default:
        text = "Error";

}
console.log(text); // Tuesday
